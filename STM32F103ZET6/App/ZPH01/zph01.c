#include "zph01.h"
//********************************************************************//
//! 函数名:FucCheckSum
//! 功能:zph01数据效验和计算函数
//! 输入:i:数据首地址指针;ln:数据长度
//! 输出:效验和
//********************************************************************//
u8 FucCheckSum(u8 *i,u8 ln)
{
  u8 j,tempq=0;
  i+=1;
  for(j=0;j<(ln-2);j++)
    {
      tempq+=*i;
      i++;
    }
  tempq=(~tempq)+1;
  return(tempq);
}
